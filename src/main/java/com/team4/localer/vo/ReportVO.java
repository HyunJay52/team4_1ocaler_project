package com.team4.localer.vo;

public class ReportVO {
	private int rep_num;
	private int num;
	private String userid;
	private String rep_content;
	private String rep_subject;
	private int rep_status;
	private String rep_ok;
	private String rep_writedate;
	public int getRep_num() {
		return rep_num;
	}
	public void setRep_num(int rep_num) {
		this.rep_num = rep_num;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getUserid() {
		return userid;
	}
	public void setUserid(String userid) {
		this.userid = userid;
	}
	public String getRep_content() {
		return rep_content;
	}
	public void setRep_content(String rep_content) {
		this.rep_content = rep_content;
	}
	public String getRep_subject() {
		return rep_subject;
	}
	public void setRep_subject(String rep_subject) {
		this.rep_subject = rep_subject;
	}
	public int getRep_status() {
		return rep_status;
	}
	public void setRep_status(int rep_status) {
		this.rep_status = rep_status;
	}
	public String getRep_ok() {
		return rep_ok;
	}
	public void setRep_ok(String rep_ok) {
		this.rep_ok = rep_ok;
	}
	public String getRep_writedate() {
		return rep_writedate;
	}
	public void setRep_writedate(String rep_writedate) {
		this.rep_writedate = rep_writedate;
	}
	
	
}
